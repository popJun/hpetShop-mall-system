public class TypeNodeDTO {
	private long id;// 商品类别id
	private String text;// 商品类别内容
	private String state;// 商品类别状态

	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}

	public String getText() {
		return text;
	}

	public void setText(String text) {
		this.text = text;
	}

	public String getState() {
		return state;
	}

	public void setState(String state) {
		this.state = state;
	}

}
